CREATE DATABASE QLBV
GO
USE QLBV

CREATE TABLE PHARMACY
(
	NT_ID VARCHAR(10) PRIMARY KEY,
	NT_NAME NVARCHAR(255) 
)

CREATE TABLE FACULTY
(
	F_ID VARCHAR(10) PRIMARY KEY,
	F_NAME NVARCHAR(255)
)

CREATE TABLE DOCTOR 
(
	D_ID VARCHAR(10) PRIMARY KEY,
	D_NAME NVARCHAR(255),
	D_AGE INT,
	D_GENDER NVARCHAR(255),
	D_ADDRESS NVARCHAR(255),
	D_POSITION NVARCHAR(255),
	F_ID VARCHAR(10) REFERENCES FACULTY(F_ID)
)

CREATE TABLE NURSE
(
	N_ID VARCHAR(10) PRIMARY KEY,
	N_NAME NVARCHAR(255),
	N_AGE INT,
	N_GENDER NVARCHAR(255),
	N_ADDRESS NVARCHAR(255),
	F_ID VARCHAR(10) REFERENCES FACULTY(F_ID)
)

CREATE TABLE LAB
(
	L_ID VARCHAR(10) PRIMARY KEY,
	L_NAME NVARCHAR(255),
	F_ID VARCHAR(10) REFERENCES FACULTY(F_ID)
)

CREATE TABLE DRUG
(
	T_ID VARCHAR(10) PRIMARY KEY,
	T_NAME NVARCHAR(255),
	T_QUANTITY INT,
	T_PRICE INT,
	NT_ID VARCHAR(10) REFERENCES PHARMACY(NT_ID)
)

CREATE TABLE PATIENT
(
	P_ID VARCHAR(10) PRIMARY KEY,
	P_NAME NVARCHAR(255),
	P_AGE INT,
	P_GENDER NVARCHAR(255),
	P_ADDRESS NVARCHAR(255),
	P_PHONE VARCHAR(12),
	P_BHYT NVARCHAR(255)
)

CREATE TABLE PHARMACIST
(
	PH_ID VARCHAR(10) PRIMARY KEY,
	PH_NAME NVARCHAR(255),
	PH_AGE INT,
	PH_GENDER NVARCHAR(255),
	PH_ADDRESS NVARCHAR(255),
	NT_ID VARCHAR(10) REFERENCES PHARMACY(NT_ID)
)

CREATE TABLE LAB_SHIFT
(
	LS_ID VARCHAR(10) PRIMARY KEY,
	LS_DATE DATE,
	D_ID VARCHAR(10) REFERENCES DOCTOR(D_ID),
	N_ID VARCHAR(10) REFERENCES NURSE(N_ID),
	L_ID VARCHAR(10) REFERENCES LAB(L_ID)
)

CREATE TABLE ROOM
(
	R_ID VARCHAR(10) PRIMARY KEY,
	R_NAME VARCHAR(100),
	R_NUMBER_OF_PATIENT INT,
	F_ID VARCHAR(10) REFERENCES FACULTY(F_ID)
)

CREATE TABLE LAB_CHECK
(
	LC_ID VARCHAR(10) PRIMARY KEY,
	LC_DISEASE NVARCHAR(255),
	LC_STATUS VARCHAR(100),
	LC_DATE DATE,
	L_ID VARCHAR(10) REFERENCES LAB(L_ID),
	D_ID VARCHAR(10) REFERENCES DOCTOR(D_ID),
	P_ID VARCHAR(10) REFERENCES PATIENT(P_ID)
)

CREATE TABLE IMPATIENT
(
	I_ID VARCHAR(10) PRIMARY KEY,
	P_ID VARCHAR(10) REFERENCES PATIENT(P_ID),
	LC_ID VARCHAR(10) REFERENCES LAB_CHECK(LC_ID)
)

CREATE TABLE ROOM_BILL
(
	RB_ID VARCHAR(10) PRIMARY KEY,
	RB_DATE_IN DATE,
	RB_DATE_OUT DATE,
	RB_TOTAL_PRICE FLOAT,
	R_ID VARCHAR(10) REFERENCES ROOM(R_ID),
	I_ID VARCHAR(10) REFERENCES IMPATIENT(I_ID)
)

CREATE TABLE OUTPATIENT
(
    O_ID VARCHAR(10) PRIMARY KEY,
    P_ID VARCHAR(10) REFERENCES PATIENT(P_ID),
    LC_ID VARCHAR(10) REFERENCES LAB_CHECK(LC_ID)
)

CREATE TABLE ROOM_SHIFT
(  
      RS_ID VARCHAR(10) PRIMARY KEY,
      RS_DATE DATETIME,
      D_ID VARCHAR(10) REFERENCES DOCTOR(D_ID),
      N_ID VARCHAR(10) REFERENCES NURSE(N_ID),
      R_ID VARCHAR(10) REFERENCES ROOM(R_ID),
)

CREATE TABLE PRESCRIPTION
(
     PR_ID VARCHAR(10) PRIMARY KEY,
     LC_ID VARCHAR(10) REFERENCES LAB_CHECK(LC_ID),
     P_ID  VARCHAR(10) REFERENCES PATIENT(P_ID),
)

CREATE TABLE CHECK_BILL
(
    CB_ID VARCHAR(10) PRIMARY KEY,
    PRICE  INT,
    LC_ID VARCHAR(10) REFERENCES LAB_CHECK(LC_ID),
)

CREATE TABLE PRESCRIPTION_DETAIL
(
   PD_ID VARCHAR(10) PRIMARY KEY,
   DRUG_ID VARCHAR(10) REFERENCES DRUG(T_ID),
   DRUG_NAME VARCHAR(100),
   QUANTITY INT,
   PR_ID VARCHAR(10) REFERENCES PRESCRIPTION(PR_ID)
)

CREATE TABLE BILL_OF_PHARMACY
(
   BP_ID VARCHAR(10) PRIMARY KEY,
   BHYT_DISCOUNT DECIMAL(3,3),
   BILL_DATE DATETIME,
   NT_ID VARCHAR(10) REFERENCES PHARMACY(NT_ID),
   PH_ID VARCHAR(10) REFERENCES PHARMACIST(PH_ID),
   PR_ID VARCHAR(10) REFERENCES PRESCRIPTION(PR_ID)
)

CREATE TABLE BILL_DETAIL
(
   BD_ID VARCHAR(10) PRIMARY KEY,
   DRUG_NAME VARCHAR(255),
   QUANTITY INT,
   TOTAL_PRICE INT,
   T_ID VARCHAR(10) REFERENCES DRUG(T_ID)
)    



